#!/bin/bash
#============================================================
# 云端OMC工具 - 后端重启脚本
# 用法: ./restart.sh
#============================================================

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

echo "============================================"
echo "  云端OMC工具 - 重启后端服务"
echo "============================================"

"${SCRIPT_DIR}/stop.sh"
sleep 2
"${SCRIPT_DIR}/start.sh"
